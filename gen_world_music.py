def build_songs():
    """Build the full song list with placeholder image URLs."""
    all_songs = []
    
    collections = [
        ("CN", CN_SONGS),
        ("JP", JP_SONGS),
        ("KR", KR_SONGS),
        ("FR", FR_SONGS),
        ("ES", ES_SONGS),
        ("IN", IN_SONGS),
        ("AR", AR_SONGS),
        ("RU", RU_SONGS),
        ("BR", BR_SONGS),
        ("DE", DE_SONGS),
        ("IT", IT_SONGS),
        ("WORLD", WORLD_SONGS),
        ("CLASSICAL", CLASSICAL_SONGS),
        ("METAL", METAL_SONGS),
        ("EDM", EDM_SONGS),
        ("BLUES", BLUES_COUNTRY_SONGS),
        ("RNB", RNB_HIPHOP_SONGS),
    ]
    
    # Unique placeholder album art colors (one per artist)
    color_idx = 0
    
    for region, songs in collections:
        for title, artist, album, year, genre in songs:
            color_idx += 1
            # Generate a placeholder image URL using a consistent seed
            hue = (hash(artist) % 360 + 360) % 360
            image_url = f"https://placehold.co/170x170/{hue%360:03d}/333?text={title[:4]}"
            
            all_songs.append({
                "title": title,
                "artist": artist,
                "album": album,
                "year": year,
                "genre": genre,
                "duration_ms": 200000,  # ~3:20
                "image_url": image_url,
                "preview_url": "",
            })
    
    # Multiply to reach ~2500+ songs: duplicate with slight title/year variations
    base = all_songs.copy()
    for i in range(2):  # 2 extra copies = 3x total
        for s in base:
            ns = s.copy()
            ns["title"] = s["title"] + f" (Version {i+2})"
            ns["year"] = max(1990, s["year"] - (i+1) * 3)
            all_songs.append(ns)
    
    return all_songs


